package jsoul.midi;

/**
 * A class which provides useful values for setting velocities (i.e. volumes) of
 * Notes and Chords. Contains the most commonly used dynamic values.
 * @author Andrew Goldin
 */
public class Dynamics {

	/**
	 * A very quiet velocity.
	 */
	public static final int PIANISSIMO = 27;
	
	/**
	 * A quiet velocity.
	 */
	public static final int PIANO = 47;
	
	/**
	 * A somewhat quiet velocity.
	 */
	public static final int MEZZOPIANO = 67;
	
	/**
	 * A somewhat loud velocity.
	 */
	public static final int MEZZOFORTE = 87;
	
	/**
	 * A loud velocity.
	 */
	public static final int FORTE = 107;
	
	/**
	 * A very loud velocity.
	 */
	public static final int FORTISSIMO = 127;
	
}
